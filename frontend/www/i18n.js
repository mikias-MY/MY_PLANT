    let currentLang = DEFAULT_LANG;

    function getDict() {
        return STRINGS[currentLang] || STRINGS.en;
    }

    function t(key, vars) {
        let s = getDict()[key] || STRINGS.en[key] || key;
        if (vars && typeof vars === 'object') {
            Object.keys(vars).forEach((k) => {
                s = s.split(`{${k}}`).join(String(vars[k]));
            });
        }
        return s;
    }

    function getLang() {
        return currentLang;
    }

    function setLang(code) {
        if (!STRINGS[code]) code = DEFAULT_LANG;
        currentLang = code;
        try {
            localStorage.setItem(STORAGE_KEY, code);
        } catch (e) { }
        const html = document.documentElement;
        html.setAttribute('lang', code === 'ar' ? 'ar' : code === 'am' ? 'am' : code === 'om' ? 'om' : 'en');
        html.setAttribute('dir', code === 'ar' ? 'rtl' : 'ltr');
        html.classList.toggle('rtl', code === 'ar');
        applyDom();
        window.dispatchEvent(new CustomEvent('myplant-lang', { detail: { lang: code } }));
    }

    function applyDom() {
        document.querySelectorAll('[data-i18n]').forEach((el) => {
            const k = el.getAttribute('data-i18n');
            if (k) el.textContent = t(k);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
            const k = el.getAttribute('data-i18n-placeholder');
            if (k) el.setAttribute('placeholder', t(k));
        });
        document.querySelectorAll('[data-i18n-html]').forEach((el) => {
            const k = el.getAttribute('data-i18n-html');
            if (k) el.innerHTML = t(k).replace(/\|/g, '<br>');
        });
        document.querySelectorAll('[data-i18n-alt]').forEach((el) => {
            const k = el.getAttribute('data-i18n-alt');
            if (k) el.setAttribute('alt', t(k));
        });
        document.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
            const k = el.getAttribute('data-i18n-aria-label');
            if (k) el.setAttribute('aria-label', t(k));
        });
        document.querySelectorAll('.lang-chip').forEach((btn) => {
            const c = btn.getAttribute('data-lang');
            btn.classList.toggle('lang-chip-active', c === currentLang);
            btn.setAttribute('aria-pressed', c === currentLang ? 'true' : 'false');
        });
        const titleEl = document.querySelector('title');
        if (titleEl) titleEl.textContent = t('app_title');
    }

    function init() {
        try {
            const saved = localStorage.getItem(STORAGE_KEY);
            if (saved && STRINGS[saved]) currentLang = saved;
        } catch (e) { }
        const strip = document.getElementById('lang-strip');
        if (strip) {
            strip.querySelectorAll('.lang-chip').forEach((btn) => {
                btn.addEventListener('click', () => {
                    const code = btn.getAttribute('data-lang');
                    if (code) setLang(code);
                });
            });
        }
        applyDom();
    }

    window.MY_PLANT_I18N = {
        t,
        getLang,
        setLang,
        applyDom,
        init,
        STRINGS
    };

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }
